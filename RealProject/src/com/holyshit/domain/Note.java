package com.holyshit.domain;

public class Note {
	private String pno;
	private String noterno;
	private String notedno;
	private String notes;
	public String getPno() {
		return pno;
	}
	public void setPno(String pno) {
		this.pno = pno;
	}
	public String getNoterno() {
		return noterno;
	}
	public void setNoterno(String noterno) {
		this.noterno = noterno;
	}
	public String getNotedno() {
		return notedno;
	}
	public void setNotedno(String notedno) {
		this.notedno = notedno;
	}
	public String getNotes() {
		return notes;
	}
	public void setNotes(String notes) {
		this.notes = notes;
	}
}
