package com.holyshit.domain;

public class Qualification {
	private String qualifno;
	private String staffno;
	private String qualifdesc;
	public String getQualifno() {
		return qualifno;
	}
	public void setQualifno(String qualifno) {
		this.qualifno = qualifno;
	}
	public String getStaffno() {
		return staffno;
	}
	public void setStaffno(String staffno) {
		this.staffno = staffno;
	}
	public String getQualifdesc() {
		return qualifdesc;
	}
	public void setQualifdesc(String qualifdesc) {
		this.qualifdesc = qualifdesc;
	}
	
}
